    private void cadastrarAdminButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarAdminButtonActionPerformed
        // TODO add your handling code here:
        
            String nome_usuario, senha_usuario;

            nome_usuario = casdatroAdminTextField.getText();
            senha_usuario = new String(adminSenhaPasswordField.getPassword());

            UsuarioDTO objusuariodto = new UsuarioDTO();
            objusuariodto.setCadastrar_user(nome_usuario);
            objusuariodto.setCadastrar_senha(senha_usuario);
            
            UsuarioDao objusuariodao = new UsuarioDao();
            
            ResultSet repetido = objusuariodao.adminRepetido(objusuariodto);
            
        try {
            if(repetido.next()){
                JOptionPane.showMessageDialog(null,"Esse admin ja existe tente outro");
            
            }
            
            else{
                int rsusuariodao = objusuariodao.adminInsert(objusuariodto);            
                if(rsusuariodao>=0)JOptionPane.showMessageDialog(null,"Admin cadastrado");
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Cadastro view "+erro);
        }
    }//GEN-LAST:event_cadastrarAdminButtonActionPerformed

